#[cfg(test)]
mod tests {
    use crate::packet::{IpPacket, Protocol};

    #[test]
    fn test_ipv4_packet_creation() {
        let ipv4_packet = [
            // IPv4 header (20 bytes)
            0x45, 0x00, 0x00, 0x54, // Version, IHL, DSCP, ECN, Total length
            0x00, 0x01, 0x40, 0x00, // ID, Flags, Fragment offset
            0x40, 0x06, 0x00, 0x00, // TTL, Protocol (TCP), Checksum
            0xc0, 0xa8, 0x01, 0x01, // Source IP: 192.168.1.1
            0xc0, 0xa8, 0x01, 0x02, // Dest IP: 192.168.1.2
            // TCP header + payload (34 bytes)
            0x00, 0x50, 0x00, 0x50, // Src port, Dest port
            0x00, 0x00, 0x00, 0x00, // Seq number
            0x00, 0x00, 0x00, 0x00, // Ack number
            0x50, 0x02, 0x20, 0x00, // Offset, Flags, Window
            0x00, 0x00, 0x00, 0x00, // Checksum, Urgent pointer
            0x00, 0x00, 0x00, 0x00, // Payload
            0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00,
            0x00, 0x00,
        ];

        let packet = IpPacket::parse(&ipv4_packet);
        assert!(packet.is_some());

        let packet = packet.unwrap();
        assert_eq!(packet.protocol, Protocol::Tcp);
        assert_eq!(packet.src_port, Some(80));
        assert_eq!(packet.dst_port, Some(80));
    }

    #[test]
    fn test_connection_key() {
        let ipv4_packet = [
            0x45, 0x00, 0x00, 0x54, 0x00, 0x01, 0x40, 0x00,
            0x40, 0x06, 0x00, 0x00, 0xc0, 0xa8, 0x01, 0x01,
            0xc0, 0xa8, 0x01, 0x02, 0x00, 0x50, 0x00, 0x50,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x50, 0x02, 0x20, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
            0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        ];

        if let Some(packet) = IpPacket::parse(&ipv4_packet) {
            let key = packet.connection_key();
            assert!(key.contains("192.168.1.1:80->192.168.1.2:80"));
        }
    }
}
